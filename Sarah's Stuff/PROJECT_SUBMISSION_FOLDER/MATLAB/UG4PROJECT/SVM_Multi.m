 Fisher_SVM_Precision_micro,...
 Fisher_SVM_Recall_Macro,...
 Fisher_SVM_Recall_micro,...
 Fisher_SVM_F1Score_Macro,...
 Fisher_SVM_F1Score_micro, ...
 Fisher_SVM_Final__Accuracy,...
 Fisher_SVM_Best_Cost,...
 Fisher_SVM_Best_Gamma,...
