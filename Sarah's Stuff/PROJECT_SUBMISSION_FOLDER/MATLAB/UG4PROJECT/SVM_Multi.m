 CFS_SVM_Final__Accuracy,...
 CFS_SVM_Best_Cost,...
 CFS_SVM_Best_Gamma,...
 CFS_SVM_Final_ConfMatrix,...
 CFS_SVM_Final_Inconclusive] ...
    = RunMySVM(CFS_M,InY);

WriteMySVMFile(ChosenFeatures,...
'CFS_Matrix',...
'MultiClass',...
CFS_SVM_Precision_Macro,...
